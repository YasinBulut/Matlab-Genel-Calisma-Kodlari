#include <iostream>
using namespace std;


//arttýrma operatörleri

int main(int argc, char** argv) {
	int a=100;
	int b=200;
	int c = 300;
	cout<<a<<endl; //100
	cout<<a++<<endl; //100
	cout<<a<<endl; //101
	
	cout<<b<<endl; //200
	cout<<++b<<endl; //201
	cout<<b<<endl;//201
	
	cout<<c<<endl;//300
	c++;
	cout<<c<<endl;//301
	
	
	
	
	
	return 0;
}
